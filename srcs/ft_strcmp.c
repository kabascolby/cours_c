int		ft_strcmp(char *s1, char *s2)
{
	while (*s1++ == *s2++)
		if (*s1 == '\0' && *s2 == '\0')
			return (0);
	return (*--s1 - *--s2);
}

int main(int argc, char *argv[])
{
	ft_strcmp("lamine", "kaba");
	return(0);
}